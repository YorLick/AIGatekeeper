// Contador de tests
static int tests_passed = 0;
static int tests_failed = 0;

#define TEST(name)                                                        \
    do {                                                                  \
        std::cout << "  TEST: " << name << "... ";                        \
        try {                                                             \
            { /* scope for test body */

#define END_TEST                                                          \
            }                                                             \
            std::cout << "✅ OK\n";                                       \
            tests_passed++;                                               \
        } catch (const std::exception& e) {                               \
            std::cout << "❌ FAIL: " << e.what() << "\n";                 \
            tests_failed++;                                               \
        }                                                                 \
    } while(0)

void assert_contains(const std::string& haystack, const std::string& needle) {
    if (haystack.find(needle) == std::string::npos) {
        throw std::runtime_error(
            "Expected '" + needle + "' in:\n---\n" + haystack + "\n---"
        );
    }
}

void assert_not_contains(const std::string& haystack, const std::string& needle) {
    if (haystack.find(needle) != std::string::npos) {
        throw std::runtime_error(
            "Did NOT expect '" + needle + "' in:\n---\n" + haystack + "\n---"
        );
    }
}

